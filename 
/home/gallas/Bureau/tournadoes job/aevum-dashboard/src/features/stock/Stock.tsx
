// Colors for charts
const COLORS = {
  available: '#3ecf8e',
  assigned: '#6490ff',
  maintenance: '#fb923c',
  critical: '#e05050',
};

const CategoryColors: Record<string, string> = {
  informatique: '#6490ff',
  mobilite: '#3ecf8e',
  mobilier: '#a78bfa',
  equipements: '#fb923c',
};

// KPI Card Component
interface KPICardProps {
  label: string;
  value: string | number;
  subValue?: string;
  icon: string;
  color: string;
  trend?: 'up' | 'down' | 'stable';
}

const KPICard: React.FC<KPICardProps> = ({ label, value, subValue, icon, color, trend }) => {
  return (
    <div style={{ 
      ...CardStyles.default, 
      position: 'relative',
      overflow: 'hidden',
    }}>
      <div style={{ 
        position: 'absolute', 
        inset: 0, 
        borderRadius: BorderRadius.xxl,
        background: 'linear-gradient(90deg, transparent, rgba(100,140,255,0.03), transparent)',
        backgroundSize: '200%',
      }} />
      <div style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'flex-start', marginBottom: 12 }}>
        <div style={{
          width: 36, height: 36, borderRadius: 8,
          background: `${color}18`, border: `1px solid ${color}33`,
          display: 'flex', alignItems: 'center', justifyContent: 'center',
          fontSize: 16, color
        }}>{icon}</div>
        {trend && (
          <div style={{
            padding: '3px 8px', borderRadius: 6,
            background: trend === 'up' ? 'rgba(62,207,142,0.1)' : trend === 'down' ? 'rgba(224,80,80,0.1)' : 'rgba(100,140,255,0.1)',
            color: trend === 'up' ? Colors.green : trend === 'down' ? Colors.red : Colors.accent,
            fontSize: 10, fontFamily: "'DM Sans',sans-serif", fontWeight: 600,
          }}>
            {trend === 'up' ? '▲' : trend === 'down' ? '▼' : '—'} {subValue}
          </div>
        )}
      </div>
      <div style={{ 
        fontFamily: "'DM Serif Display',Georgia,serif", 
        fontSize: 26,
        fontWeight: 700, 
        color: Colors.text, 
        marginBottom: 4 
      }}>{value}</div>
      <div style={{ fontSize: 11, color: Colors.textMuted, fontFamily: "'DM Sans',sans-serif" }}>{label}</div>
    </div>
  );
};

